## Topic Essentials
Swift lets you create variables for storing values and referencing them by name. Depending on how you declare a variable, its value is either a constant or can be changed at a later time.
 + Using the `var` keyword means the variable value can be changed at any time.
 + Using the `let` keyword means the variable is a constant and cannot be changed once its value is assigned.
 
